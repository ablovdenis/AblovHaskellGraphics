module Transliteration (transliteration) where

-- Функция транслитерации (ГОСТ 7.79-2000 / ISO 9)
-- Признаюсь честно: мне эту функцию DeepSeek сделал.
transliteration :: String -> String
transliteration = concatMap translitChar
  where
    translitChar 'А' = "A"
    translitChar 'а' = "a"
    translitChar 'Б' = "B"
    translitChar 'б' = "b"
    translitChar 'В' = "V"
    translitChar 'в' = "v"
    translitChar 'Г' = "G"
    translitChar 'г' = "g"
    translitChar 'Д' = "D"
    translitChar 'д' = "d"
    translitChar 'Е' = "E"
    translitChar 'е' = "e"
    translitChar 'Ё' = "Yo"
    translitChar 'ё' = "yo"
    translitChar 'Ж' = "Zh"
    translitChar 'ж' = "zh"
    translitChar 'З' = "Z"
    translitChar 'з' = "z"
    translitChar 'И' = "I"
    translitChar 'и' = "i"
    translitChar 'Й' = "Y"
    translitChar 'й' = "y"
    translitChar 'К' = "K"
    translitChar 'к' = "k"
    translitChar 'Л' = "L"
    translitChar 'л' = "l"
    translitChar 'М' = "M"
    translitChar 'м' = "m"
    translitChar 'Н' = "N"
    translitChar 'н' = "n"
    translitChar 'О' = "O"
    translitChar 'о' = "o"
    translitChar 'П' = "P"
    translitChar 'п' = "p"
    translitChar 'Р' = "R"
    translitChar 'р' = "r"
    translitChar 'С' = "S"
    translitChar 'с' = "s"
    translitChar 'Т' = "T"
    translitChar 'т' = "t"
    translitChar 'У' = "U"
    translitChar 'у' = "u"
    translitChar 'Ф' = "F"
    translitChar 'ф' = "f"
    translitChar 'Х' = "Kh"
    translitChar 'х' = "kh"
    translitChar 'Ц' = "Ts"
    translitChar 'ц' = "ts"
    translitChar 'Ч' = "Ch"
    translitChar 'ч' = "ch"
    translitChar 'Ш' = "Sh"
    translitChar 'ш' = "sh"
    translitChar 'Щ' = "Sch"
    translitChar 'щ' = "sch"
    translitChar 'Ъ' = "'"
    translitChar 'ъ' = "'"
    translitChar 'Ы' = "Y"
    translitChar 'ы' = "y"
    translitChar 'Ь' = "'"
    translitChar 'ь' = "'"
    translitChar 'Э' = "E"
    translitChar 'э' = "e"
    translitChar 'Ю' = "Yu"
    translitChar 'ю' = "yu"
    translitChar 'Я' = "Ya"
    translitChar 'я' = "ya"
    translitChar c = [c]  -- не русские символы оставляем как есть